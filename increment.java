public class index {
	public static void main(String[] args) {
		int x=10;
		System.out.println(x++); // print and increment
		System.out.println(++x); // increment and print
		System.out.println(x--); // print and decrement
		System.out.println(--x); // decrement and print
		System.out.println(x);   // print as it is
	}
}
